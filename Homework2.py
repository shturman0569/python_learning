s = "Hho is this?"
print(s[2]) # третий символ этой строки.
print(s[-2]) # предпоследний символ этой строки.
print(s[0:5])# первые пять символов этой строки.
print(s[0:-2]) # вся строка, кроме последних двух символов.
print(s[0:10]) # вся строка, кроме последних двух символов.
print(s[:10]) # вся строка, кроме последних двух символов.
print(s[0:12:2]) # все символы с четными индексами (считая, что индексация начинается с 0, поэтому символы выводятся, начиная с первого).
print(s[1:12:2]) # все символы с нечетными индексами, то есть начиная со второго символа строки.
print(s[::-1]) # все символы в обратном порядке.
print(s[::-2]) # все символы строки через один в обратном порядке, начиная с последнего.
print(len(s)) # длина данной строки.